import java.util.*;

class HelloWorld {
    public static void printArray(int arr[]) {
        for (int i = 0; i < arr.length; i++) {
            System.out.print(arr[i]+" ");
        }
        System.out.println();
    }
    public static void main(String[] args) {
        int arr[] = {5,2,7,1,3,0};
        
        for (int i = 0; i < arr.length - 1; i++) {
            int min = i;
            for (int j = i + 1; j < arr.length; j++) {
                if (arr[j] < arr[min]) {
                    min = j;
                }
            }
            if (i != min) {
            int temp = arr[min];
            arr[min] = arr[i];
            arr[i] = temp;
            }
        }
        printArray(arr);
    }
}